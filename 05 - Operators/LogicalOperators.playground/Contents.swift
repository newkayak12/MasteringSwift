import UIKit

/*:
 # Logical Operators
 */


/*:
 ## Logical NOT Operators
 ````
 !a
 ````
 */

!true
let a: Int = 12;
let b: Int = 34;

a < b;
!(a < b);
/*:
 ## Logical AND Operators
 ````
 a && b
 ````
 */

(a > 30) && (b % 2 == 0)

true && true;
true && false;
false && true;
false && false;

/*:
 ## Logical OR Operators
 ````
 a || b
 ````
 */

(a > 30) || (b % 2 == 0)

true || true;
true || false;
false || true;
false || false;
















