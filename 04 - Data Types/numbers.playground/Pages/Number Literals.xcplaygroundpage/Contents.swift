import UIKit

/*:
 # Number Literals
 */
// -> 양수 +를 붙이면 된다. 물론 생략할 수 있다.
1234
// -> 음수 -를 붙인다. 생략할 수 없다.
-1234
// 1.23e4와 같이 십진수 지수 실수를 사용할 수 있다.
1.423e19
// 0xAp2 16인수는 p 다음 지수를 사용하면 된다.
0xFp4
// 1_000_000와 같이 ','을 _로 사용하면 된다.
20_000_000
// 2진수는 0b를 접두어로 사용한다.
0b1010
// 8진수는 0o를 접두어로 사용한다.
0o12
// 16진수는 0x를 접두어로 사용한다.
0xFFFF


